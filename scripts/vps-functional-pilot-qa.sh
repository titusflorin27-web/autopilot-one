 echo "=== RECEPTION AI INTERNAL MESSAGE ==="
if [ "$RUN_RECEPTION_AI_MESSAGE" = "1" ]; then
  cat > "$TMP_DIR/reception-message.json" <<JSON
{
  "organizationId": "$ORG_ID",
  "message": "Bună, vreau o ofertă pentru recepționer AI, widget pe website și CRM Lite. Mă puteți contacta mâine?",
  "customerName": "Client QA $RUN_ID",
  "customerEmail": "client-$RUN_ID@example.com",
  "channel": "build-078a-functional-pilot-qa"
}
JSON
  api_json POST /reception-ai/message "$TMP_DIR/reception-message.json" "$TMP_DIR/reception-response.json" "200 201" auth
  CONVERSATION_ID="$(json_get_file "$TMP_DIR/reception-response.json" conversationId)" || fail "could not read conversationId from reception response"
  echo "conversationId=$CONVERSATION_ID"
