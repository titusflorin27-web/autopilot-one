  echo "Reception AI message skipped. Set RUN_RECEPTION_AI_MESSAGE=1 to run it."
fi

echo
 echo "=== INBOX / LEADS / TASKS / DASHBOARD READS ==="
api_json GET "/reception-ai/organization/$ORG_ID/summary" "" "$TMP_DIR/reception-summary.json" "200" auth
api_json GET "/reception-ai/organization/$ORG_ID/conversations" "" "$TMP_DIR/reception-conversations.json" "200" auth
api_json GET "/reception-ai/organization/$ORG_ID/leads" "" "$TMP_DIR/reception-leads.json" "200" auth
api_json GET "/reception-ai/organization/$ORG_ID/tasks" "" "$TMP_DIR/reception-tasks.json" "200" auth
api_json GET "/inbox/organization/$ORG_ID/conversations" "" "$TMP_DIR/inbox-conversations.json" "200" auth
api_json GET "/dashboard/organization/$ORG_ID" "" "$TMP_DIR/dashboard.json" "200" auth

echo
 echo "=== BILLING READS ==="
api_json GET "/billing/organization/$ORG_ID" "" "$TMP_DIR/billing.json" "200" auth

echo
 echo "=== OUTPUT SUMMARY ==="
echo "Temporary QA files: $TMP_DIR"
echo "Pilot email: $PILOT_EMAIL"
echo "Pilot password was generated for this run only. Do not reuse it."
echo "Pilot organization id: $ORG_ID"
echo "Pilot organization slug: $PILOT_ORG_SLUG"

echo
 echo "=== FUNCTIONAL PILOT QA PASSED ==="
