api_json GET "/organizations/$ORG_ID/widget-settings" "" "$TMP_DIR/widget-settings.json" "200" auth

echo
 echo "=== KNOWLEDGE BASE TEXT SOURCE ==="
cat > "$TMP_DIR/kb-text-source.json" <<JSON
{
  "organizationId": "$ORG_ID",
  "title": "Build 078A pilot QA services",
  "content": "Autopilot QA offers reception AI, website widget, CRM Lite, lead follow-up and guided onboarding. Pilot support is available in Romanian and English.",
  "fileName": "build-078a-pilot-qa.txt"
}
JSON
api_json POST /knowledge-base/text "$TMP_DIR/kb-text-source.json" "$TMP_DIR/kb-create-response.json" "200 201" auth
api_json GET "/knowledge-base/organization/$ORG_ID/sources" "" "$TMP_DIR/kb-sources.json" "200" auth

cat > "$TMP_DIR/kb-search.json" <<JSON
{
  "organizationId": "$ORG_ID",
  "query": "reception AI CRM Lite pilot support",
  "limit": 5
}
JSON
api_json POST /knowledge-base/search "$TMP_DIR/kb-search.json" "$TMP_DIR/kb-search-response.json" "200 201" auth

